public class Player{
	private int fieldA;
	private int fieldB;
	private int fieldC;
	private int fieldD;

	public Player(int fieldA, int fieldB, int fieldC, int fieldD){
		this.fieldA = fieldA;
		this.fieldB = fieldB;
		this.fieldC = fieldC;
		this.fieldD = fieldD;
	}

	public int getFieldA(){
		return fieldA;
	}

	public int getFieldB(){
		return fieldB;
	}

	public int getFieldC(){
		return fieldC;
	}

	public int getFieldD(){
		return fieldD;
	}

}